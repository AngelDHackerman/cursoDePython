# Comparador and

es_estudiante = True
trabaja = False

print(es_estudiante and trabaja) # False

# Comparador or

print(es_estudiante or trabaja) # True

# Comparador not

print(not trabaja) # True



numero1 = 5
numero2 = 5
numero3 = 7

# Comparador de igualdad == 

print(numero1 == numero2) # True

# Comparador de negacion != 

print(numero2 != numero3) # True

